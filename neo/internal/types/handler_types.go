package types

import "net"

// TCPHandler 连接处理器接口
// 定义TCP连接的启动和停止标准操作
// +----------------+-----------------------------------+
// | 方法名         | 描述                              |
// +----------------+-----------------------------------+
// | Start          | 启动处理器，开始处理连接请求      |
// | Stop           | 停止处理器，释放相关资源          |
// +----------------+-----------------------------------+
type TCPHandler interface {
	Start() error
	Stop()
}

// TCPHandlerFactory 创建处理器的工厂函数类型
// 根据传入的网络连接创建对应的TCPHandler实例
// +----------------+-----------------------------------+
// | 参数           | 描述                              |
// +----------------+-----------------------------------+
// | conn           | 底层网络连接对象                  |
// | 返回值         | 创建的TCPHandler实例              |
// +----------------+-----------------------------------+
type TCPHandlerFactory func(conn net.Conn) TCPHandler
