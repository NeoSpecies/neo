package types

import (
	"context"
)

// Close 关闭指标服务器并释放相关资源
// 停止HTTP服务器并释放所有指标收集相关资源
// +----------------+------------------+-----------------------------------+
// | 参数           | 类型             | 描述                              |
// +----------------+------------------+-----------------------------------+
// | ctx            | context.Context  | 上下文对象，用于控制关闭超时      |
// +----------------+------------------+-----------------------------------+
// +----------------+-----------------------------------+
// | 返回值         | 描述                              |
// +----------------+-----------------------------------+
// | error          | 关闭过程中发生的错误，如超时等    |
// +----------------+-----------------------------------+
func (m *Metrics) Close(ctx context.Context) error {
	if m.Server != nil {
		return m.Server.Shutdown(ctx)
	}
	return nil
}
